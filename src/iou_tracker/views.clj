(ns iou-tracker.views
  (:require [hiccup.core :refer [html]]
            [hiccup.page :refer [html5 include-css]]
            [cheshire.core :as json]))

;; ---------------------------------------------------------------------------
;; Shared layout
;; ---------------------------------------------------------------------------

(defn- page [title & body]
  (html5
    [:head
     [:meta {:charset "utf-8"}]
     [:meta {:name "viewport" :content "width=device-width, initial-scale=1"}]
     [:title title]
     (include-css "/style.css")]
    (into [:body] body)))

;; ---------------------------------------------------------------------------
;; Landing page
;; ---------------------------------------------------------------------------

(defn landing []
  (page "IOU Tracker"
    [:div.container
     [:h1 "IOU Tracker"]
     [:p.tagline "Track shared expenses by SMS — no app, no bank account needed."]
     [:div.steps
      [:h2 "How it works"]
      [:ol
       [:li "Text your IOU to the number: " [:code "17.50 for pizza from Maria"]]
       [:li "Get an instant confirmation by SMS"]
       [:li "Text " [:code "balance"] " any time to see what you owe"]
       [:li "Text " [:code "settle Maria"] " to get a printable receipt"]
       [:li "Meet in person, exchange cash, tap the receipt to mark it settled"]]]
     [:div.commands
      [:h2 "Commands"]
      [:table
       [:thead [:tr [:th "Text"] [:th "What it does"]]]
       [:tbody
        [:tr [:td [:code "{amount} for {item} from {name}"]] [:td "Record a debt"]]
        [:tr [:td [:code "balance"]]                          [:td "See all you owe"]]
